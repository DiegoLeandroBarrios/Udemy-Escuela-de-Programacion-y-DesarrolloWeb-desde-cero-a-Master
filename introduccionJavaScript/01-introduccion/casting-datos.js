//conversion de datos

// de entero a string

let entero = 10;
console.log(typeof entero);

entero = String(entero);
console.log(typeof entero);

//string a entero

entero = "45";
console.log(typeof entero);

entero = Number(entero);
console.log(typeof entero);

//string a float

entero = "4.5";
console.log(typeof entero);

entero = parseFloat(entero);
console.log(typeof entero);
